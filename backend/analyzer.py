# 메인 추출 함수
# ═══════════════════════════════════════════════════════════════


def extract_keywords_from_title(title: str) -> str:
    """워치리스트 우선 매칭 → fallback 자유 추출. 쉼표 구분 문자열 반환."""
    watchlist_hits = []
    remaining = title

    # 1단계: 워치리스트 매칭 (긴 키워드부터)
    for pat, kw in _PATTERNS:
        if pat.search(remaining):
            watchlist_hits.append(kw)
            remaining = pat.sub("", remaining)

    # 2단계: 워치리스트 매칭 결과가 있으면 그것만 반환
    if watchlist_hits:
        return ",".join(watchlist_hits)

    # 3단계: 없으면 fallback 자유 추출
    fallback_hits = _fallback_extract(title)
    return ",".join(fallback_hits[:5])  # 최대 5개


def get_top_keywords(
    keyword_source_pairs: list,
    top_n: int = 20,
    min_count: int = 1,
    min_sources: int = 1,
) -> list:
    """
    (keyword_string, source_name) 튜플 리스트에서
    TOP N 키워드를 빈도수 + 언론사 수와 함께 반환.
    """
    counter: Counter = Counter()
    source_map: dict = defaultdict(set)

    for ks, source in keyword_source_pairs:
        if not ks:
            continue
        for kw in ks.split(","):
            kw = kw.strip()
            if kw:
                counter[kw] += 1
                source_map[kw].add(source)

    results = []
    for kw, cnt in counter.most_common(top_n * 3):
        sc = len(source_map[kw])
        if cnt >= min_count and sc >= min_sources:
            results.append({
                "keyword": kw,
                "count": cnt,
                "source_count": sc,
            })
        if len(results) >= top_n:
            break

    return results
