_SORTED_KW = sorted(WATCHLIST, key=len, reverse=True)

_PATTERNS = []
for kw in _SORTED_KW:
    if re.match(r"^[A-Za-z0-9]", kw):
        # 영문/숫자: 단어 경계 + 대소문자 무시
        _PATTERNS.append((re.compile(r"(?i)\b" + re.escape(kw) + r"\b"), kw))
    else:
        # 한글: 단순 포함 매칭
        _PATTERNS.append((re.compile(re.escape(kw)), kw))


# ═══════════════════════════════════════════════════════════════
# 자유 추출 fallback — 3글자 이상 한글 명사 + 영문 약어
# ═══════════════════════════════════════════════════════════════

# 워치리스트에 없을 때 fallback으로 사용할 기본 패턴
_FALLBACK_KR = re.compile(r"[가-힣]{3,7}")
_FALLBACK_EN = re.compile(r"\b[A-Z][A-Za-z0-9]{1,10}\b")

# fallback에서 제외할 불용어
_STOPWORDS = {
    "에서", "으로", "까지", "에게", "한다", "했다", "이다", "있다",
    "없다", "된다", "됐다", "하는", "되는", "위한", "통해", "따른",
    "관련", "대한", "이번", "지난", "오늘", "내일", "올해", "내년",
    "최근", "현재", "기자", "뉴스", "신문", "보도", "발표", "전했다",
    "밝혔다", "알려졌다", "나타났다", "지적했다", "설명했다",
    "따르면", "관계자", "측은", "것으로", "가능성", "우려",
    "예정이다", "전망이다", "것이다", "한편", "이날",
}


def _fallback_extract(title: str) -> list:
    """워치리스트에 매칭되지 않은 텍스트에서 자유 키워드 추출."""
    found = []
    # 한글 3~7글자 명사 후보
    for m in _FALLBACK_KR.finditer(title):
        word = m.group()
        if word not in _STOPWORDS:
            found.append(word)
    # 영문 약어 (대문자 시작, 2~10자)
    for m in _FALLBACK_EN.finditer(title):
        word = m.group()
        if word not in _STOPWORDS and len(word) >= 2:
            found.append(word)
    return found


